package boardgame.model;

/**
 * Egy bábu/korong lehetséges értékeit reprezentáló enum osztály.
 *
 */
public enum Disk {

    /**
     * A mező üres.
     */
    NONE,

    /**
     * A mezőn kék korong/bábu van.
     */
    BLUE,

    /**
     * A mezőn piros korong/bábu van.
     */
    RED,

    /**
     * A mező fekete, azaz tiltott.
     */
    BLACK
}
